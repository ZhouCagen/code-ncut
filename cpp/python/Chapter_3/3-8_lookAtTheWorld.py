touristattractions=["New York","Paris","Tokyo","Sydney","Rome","London","Dubai"]

print(touristattractions)
print("Tourist Attractions:",end=" ")
for touristattraction in touristattractions:
    print(touristattraction,end=" ")

sorted(touristattractions)
print("\n")
print(touristattractions)
print("Tourist Attractions:",end=" ")
for touristattraction in touristattractions:
    print(touristattraction,end=" ")

sorted(touristattractions,reverse=True)
print("\n")
print(touristattractions)
print("Tourist Attractions:",end=" ")
for touristattraction in touristattractions:
    print(touristattraction,end=" ")

touristattractions.reverse()
print("\n")
print(touristattractions)
print("Tourist Attractions:",end=" ")
for touristattraction in touristattractions:
    print(touristattraction,end=" ")

touristattractions.sort()
print("\n")
print(touristattractions)
print("Tourist Attractions:",end=" ")
for touristattraction in touristattractions:
    print(touristattraction,end=" ")